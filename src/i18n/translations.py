def t(lang: str, key: str, **kwargs) -> str:
    """Get translated string. Falls back to English if key missing."""
    text = TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(
        key, TRANSLATIONS["en"].get(key, key)
    )
    if kwargs:
        try:
            return text.format(**kwargs)
        except KeyError:
            return text
    return text
